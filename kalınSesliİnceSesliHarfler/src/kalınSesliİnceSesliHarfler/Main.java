package kalınSesliİnceSesliHarfler;

public class Main {

	public static void main(String[] args) {
		//kalın sesli ve ince sesli harfler
		char harf = 'ı';
		switch (harf) {
		case 'A':
		case 'O':
		case 'I':
		case 'U':
			System.out.println("kalın sesli harftir.");
			break;
			default :
			System.out.println("ince sesli harftir.");
			
		}
		
		

	}

}
